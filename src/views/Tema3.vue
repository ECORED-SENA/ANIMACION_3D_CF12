          p(data-aos="fade-down") En una producción audiovisual se realizan varias tomas o <i>renders</i>, con diferentes tipos de iluminación, en exteriores e interiores, con diferentes cámaras, Hdri, materiales, entre otros elementos, que genera que no haya una homogeneidad en la producción final, por ello se necesitan realizar unos ajustes a cada una de esas tomas, para crear un contenido similar, sin que haya saltos bruscos entre tomas. Se pueden encontrar tomas sobreexpuestas, en las que la iluminación se revienta, o por el contrario imágenes que estén oscuras.

